function normalizePunctuation(text) {
if (!text) return text;
return text
.replace(/\s*,\s*\./g, ".")
.replace(/\s*\.\s*,/g, ".")
.replace(/([.,!?])\1+/g, "$1")
.replace(/\s+([.,!?])/g, "$1")
.replace(/([.,!?])([A-Za-z])/g, "$1 $2")
.replace(/([.!?]\s+)([a-z])/g, (_, p1, p2) => p1 + p2.toUpperCase())
.replace(/^\s*([a-z])/g, m => m.toUpperCase())
.trim();
}
